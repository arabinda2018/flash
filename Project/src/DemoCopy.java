
public class DemoCopy 
{
	public static void main(String[] args) {
		int n1=10;
		int n2 =40;
		int n3=50;
		System.out.println(n1);
		System.out.println(n2);
		System.out.println(n3);
		n1=n2;
		n2=n3;
		n3=n1;
		System.out.println(n1);
		System.out.println(n2);
		System.out.println(n3);
		}
}
